package com.bancolombia.mercadolibreempresas.models;

public class SecondScreen {
	private String tasa;
	private String factor;
	private String amountInPesos;
	private String commissionTarifa;
	private String commission;

	public String getTasa() {
		return tasa;
	}

	public void setTasa(String tasa) {
		this.tasa = tasa;
	}

	public String getFactor() {
		return factor;
	}

	public void setFactor(String factor) {
		this.factor = factor;
	}

	public String getAmountInPesos() {
		return amountInPesos;
	}

	public void setAmountInPesos(String amountInPesos) {
		this.amountInPesos = amountInPesos;
	}

	public String getCommissionTarifa() {
		return commissionTarifa;
	}

	public void setCommissionTarifa(String commissionTarifa) {
		this.commissionTarifa = commissionTarifa;
	}

	public String getCommission() {
		return commission;
	}

	public void setCommission(String commission) {
		this.commission = commission;
	}

}
